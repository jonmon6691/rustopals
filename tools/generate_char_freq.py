# Computes the character frequency from the webtext Corpus
from nltk.corpus import webtext

chars = [0]*256
for letter in str(webtext.raw()):
    try:
        chars[ord(letter)] += 1
    except IndexError:
        # Skip non-ascii characters
        pass

# Print the character and the score, for sanity checking
for i in range(256):
    print(f"{i} '{chr(i)}' {chars[i]}")

# Copy-pastable into rust
print(chars)

